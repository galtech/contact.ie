<h2 id="faq">FAQs</h2>
<h3>What Happened to the Email Campaigns</h3>
<p>In a previous incarnation, we ran 'Email Campaigns', pre-formatted emails that we would deliver to defined recipients about specific issues. This feature will be coming back in a future release of the app.</p>
<h3>Why don't you use a web form anymore</h3>
<p>Former version(s) of Contact.ie used webforms to deliver your emails. However, with all these emails coming from the Contact.ie server, coupled with there being a huge volume of emails, Oireachtas servers sometimes sent them to quarantine (spam folder) and left it up to individual TDs to check their quarantine folders. This new version opens whatever your default email client is (Gmail, Outlook, Yahoo, etc) and uses that, so all emails will be sent from your account and not from our servers, meaning you can now fully circumvent the Oireachtas quarantine folders (unless you have already been flagged as a spammer!)</p>
<h3>My email client won't let me send that many emails in one go</h3>
<p>Sorry, there is nothing we can do about that. We either set up webforms and take responsibility for delivering all the emails and run the risk of some ending up in spam folders, or we put the responsibility on your email client to deliver them to try to maximise delivery. If you'd like to send them in smaller bunches, here's a list of all the email addresses we have:
<ul>
  <li><a href="/lists/email-parties">Emails of TDs by Political Party</a></li>
  <li><a href="/lists/email-constituencies">Emails of TDs by Constituency</a></li>
  <li><a href="/lists/email-councils">Emails of Town and County Councillors</a></li>
</ul></p>
